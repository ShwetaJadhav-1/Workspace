
# range(start, end, displacement)
# start : is by default
# end   : There is no default value
# displacement : is by default 1

print(range(1,5))       # range(1, 5)

arr = range(1,5)
print(arr)              # range(1, 5)

range(1,5)              # 1 2   3   4
range(1,5,1)            # 1 2   3   4
range(1,10,2)           # 1  3  5   7   9

